<h2>About  New Braunfels Area Car Club</h2>
<p>In 1991, two car enthusiasts were discussing the need for a local car club.  Barney Calvert and Bennie Mills pursued the idea and encouraged several individuals who were interested in street rods to meet together and consider the formation of just such an organization.
</p>   
<p><img class="imgl borderedbox inspace-5" src="images/100_4386.jpg" alt="">
The club was organized in late 1991 and the officers elected.  The name, New Braunfels Area Car Club, was chosen in an effort to include 
as many as possible from the surrounding communities.  The purpose of the club was, and is, to encourage the preservation and enjoyment of 
cars 20 years old or older and to be able to display these cars in the area when the opportunity presents itself.  
</p>   
<p>The idea of having a <a href="swap-meet.php">swap meet</a> soon came about, and the 1st Annual New Braunfels Swap Meet and Car Show, with the NBACC and the New Braunfels Rotary Club as its organizers, was held in April, 1993.   The proceeds of this event are used for <a href="scholarships.php">scholarships</a> for area students going into automobile-related professions. </p>   
<p>The club enjoys monthly meetings, get-togethers at  area eateries on Friday and Saturday evenings, tours, and parties.   There is always something going on to remind one of the joys of old cars!
</p>
