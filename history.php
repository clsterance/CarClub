  <h2>History of the New Braunfels Swap Meet</h2>
  <p><img src="images/history2.jpg" alt="" width="225" height="148" class="imgr borderedbox inspace-5" id="Picture15" />
  	The   New Braunfels Area Car Club, along with the Rotary Club, sponsored the   first New Braunfels Swap Meet  at the Comal County
  	Fairgrounds in April 1992. Within the city limits of New Braunfels and  equipped with RV   hookups and food vendor stalls, the site is an 
	ideal place for a Swap Meet.  The first year there were  356 Swap   Meet spaces, and about 100 cars entered the Sunday Car and Truck Show.   
	The Car Corral added a great amount of interest. Most of the cars had   good, South Texas, rust-free bodies and several of them were sold by the 
	end of the Swap Meet.</p>
	
  <p><img src="images/100_1183.jpg" alt="" width="225" height="148" class="imgr borderedbox inspace-5 width-50" />
  	The   Car Club took  full responsibility for the Swap Meet in 2000. We   sublet the catering of the food and brought in the Early Ford V8 Club of 
  	San Antonio to handle the parking. With the hard work of our members,   the Swap Meet has grown every year and continues to run very   efficiently.
  	</p>
           
  <p>
  	The Swap Meet is sponsored by the New Braunfels Area Car Club, which is dedicated to the restoration and preservation of antique, classic, and
