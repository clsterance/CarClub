<h3>Location of the New Braunfels Swap Meet</h3>
<p>New Braunfels, Texas, is located on IH-35 between Austin and San Antonio:<br />
<ul>
  <li>Austin &#8212; 46 miles south on IH-35</li>
  <li>San Antonio &#8212; 30 miles north on IH-35</li>
  <li>Houston &#8212; 170 miles west on IH-10 to TX State Hwy 46 into New Braunfels</li>
  <li> Dallas/Ft. Worth &#8212; 243 miles south on IH-35</li>
  <li>El Paso &#8212; 576 miles east on IH-10 to TX State Hwy 46 into New Braunfels </li>
</ul>
 
