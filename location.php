<h3>
Comal County Fairgrounds<br />
   801 E Common St.<br />
   New Braunfels, TX 78130  </h3>                    

<p>
 Easy access from IH-35:<br />
Exit at #189.<br />
West to Common St., about 1 1/2 miles.<br />
Left on Common St. for about a mile.<br />
Fairgrounds on right with Swap Meet signs.</p>

<p>RV parking is available at the Fairgrounds. Contact the Association  directly  at 
<span class="nowrap"> <strong>830-625-1505</strong></span> (mornings). On-site parking 
is $5.00 per car. Plenty of on-street parking is available at no charge, but we ask 
that you not block private driveways and mailboxes.</p>
 
